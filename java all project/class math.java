class mathE//class name
{
    public static void main(String a[])//main method
    {
        int c=12;//integer variable c with value
        int b=6;//integer variable b with value
        System.out.println(c+b);//system will print value of c plus b
        System.out.println(c-b);//system will print value of c minus b
        System.out.println(c*b);//system will print value of c times b
        System.out.println(c/b);//system will print value of c divide by b
        System.out.println(c%b);//system will print the remainder of c divide by b
    }//end of main method
}//end of class
