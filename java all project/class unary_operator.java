class unary_operator//class name
{
    public static void main(String a[])// main method
    {
        int i=25;// integer variable i with their value
        System.out.println(i++);//the variable i will increment after 
        System.out.println(++i);//the variable i will increment before 
        System.out.println(i--);//the variable i will decrement after 
        System.out.println(--i);//the variable i will decrement before

    }//end of main method
} //end of class